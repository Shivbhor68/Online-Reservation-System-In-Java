    public void show_user(int a){
        
        ArrayList<User> list=userList(a);
        DefaultTableModel model=(DefaultTableModel)showTable.getModel();
        Object[] row=new Object[9];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getBookingID();
            row[1]=list.get(i).getCustomer_Name();
            row[2]=list.get(i).getMobile_No();
            row[3]=list.get(i).getTrain_ID();
            row[4]=list.get(i).getTrain_Name();
            row[5]=list.get(i).getSource_Address();
            row[6]=list.get(i).getDestination_Address();
            row[7]=list.get(i).getDate();
            model.addRow(row);
        }
    
    }

