    public ArrayList<User> userList(int a){
            ArrayList<User> userList=new ArrayList<>();
             try{
         Class.forName("com.mysql.cj.jdbc.Driver");  
        Connection con=DriverManager.getConnection(  "jdbc:mysql://localhost:3307/task1","root",""); 
           Statement statement;
            statement = con.createStatement();            
            ResultSet rs;
            rs = statement.executeQuery("select * from booking where bookingID='"+a+"' ");
            User user;
            
           while (rs.next()) {
               user=new User(rs.getInt("BookingID"),rs.getString("Customer_Name"),rs.getString("Mobile_No"),rs.getString("Train_ID"),rs.getString("Train_Name"),rs.getString("Source_Address"),rs.getString("Destination_Address"),rs.getString("Date"));
               userList.add(user);
              
           }
                 
           
           statement.close();
            con.close();
           
     }catch(Exception e){
         System.out.println(e);
     
     }
             return userList;
    }
    
